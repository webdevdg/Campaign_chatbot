def _build_system_prompt() -> str:
    return SYSTEM_PROMPT_TEMPLATE.format(
        summary=get_data_summary(),
        csv_data=get_csv_text(),
    )


def chat(user_message: str, conversation_history: "list[dict]") -> dict:
    system_prompt = _build_system_prompt()

    messages = [{"role": "system", "content": system_prompt}]
    for msg in conversation_history:
        messages.append({"role": msg["role"], "content": msg["content"]})
    messages.append({"role": "user", "content": user_message})

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=messages,
        temperature=0.2,
        max_tokens=4096,
    )

    content = response.choices[0].message.content.strip()

    report_action = _parse_report_action(content)
    if report_action and "parameters" in report_action:
        return {"type": "report", "parameters": report_action["parameters"]}

    return {"type": "text", "content": content}


def _parse_report_action(content: str):
    # Strategy 1: entire response is JSON (optionally in code fences)
    cleaned = content.strip()
    cleaned = re.sub(r"^```(?:json)?\s*", "", cleaned)
    cleaned = re.sub(r"\s*```$", "", cleaned)

    try:
        data = json.loads(cleaned)
        if isinstance(data, dict) and data.get("action") == "generate_report":
            return data
    except (json.JSONDecodeError, TypeError):
        pass

    # Strategy 2: JSON embedded in code fences within surrounding text
    fence_match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", content, re.DOTALL)
    if fence_match:
        try:
            data = json.loads(fence_match.group(1))
            if isinstance(data, dict) and data.get("action") == "generate_report":
                return data
        except (json.JSONDecodeError, TypeError):
            pass

    # Strategy 3: find "generate_report" and extract the enclosing JSON by brace matching
    idx = content.find('"generate_report"')
    if idx == -1:
        return None

    # Walk backwards to find the opening brace
    start = content.rfind("{", 0, idx)
    if start == -1:
        return None

    # Walk forward matching braces to find the closing brace
    depth = 0
    for i in range(start, len(content)):
        if content[i] == "{":
            depth += 1
        elif content[i] == "}":
            depth -= 1
            if depth == 0:
                try:
                    data = json.loads(content[start:i + 1])
                    if isinstance(data, dict) and data.get("action") == "generate_report":
                        return data
                except (json.JSONDecodeError, TypeError):
                    pass
                break

    return None
